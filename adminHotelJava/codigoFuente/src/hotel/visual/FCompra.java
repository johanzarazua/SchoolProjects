    private void botonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonGuardarActionPerformed
        //validamos que todos los campos de texto tengan un valor
        if(txtServicio.getText().length() == 0){
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar un servicio");
            btnBuscarServicio.requestFocus();
            return;
        }
        if (txtCantidad.getText().length() == 0){
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar una cantidad");
            txtCantidad.requestFocus();
            return;
        }
        if (txtPrecio.getText().length() == 0){
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar un precio");
            txtPrecio.requestFocus();
            return;
        }

        Compra c = new Compra();
        CCompra cc = new CCompra();
        //guardamos los valores en el objeto de la clase Servicio.
        c.setIdReservacion(Integer.parseInt(txtIDReservacion.getText()));
        c.setIdServicio(Integer.parseInt(txtIDServicio.getText()));
        c.setFecha_compra(Date.valueOf(fecha));
        c.setCantidad(Integer.parseInt(txtCantidad.getText()));
        c.setPrecio(Double.parseDouble(txtPrecio.getText()));
        c.setEstado((String)cboEstado.getItemAt(cboEstado.getSelectedIndex()));
        
        
        if (accion.equals("guardar")){
            if (cc.insertar(c)){
                JOptionPane.showMessageDialog(rootPane, "La compra se registro correctamente");
                mostrar(idReservacion);
                inhabilitar();
            }
        }else if (accion.equals("editar")){
            c.setIdCompra(Integer.parseInt(txtIDCompra.getText()));
            if (cc.editar(c)){
                JOptionPane.showMessageDialog(rootPane, "La compra se edito correctamente");
                mostrar(idReservacion);
                inhabilitar();
            }
        }

    }//GEN-LAST:event_botonGuardarActionPerformed

