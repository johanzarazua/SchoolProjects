    /**
     * El metodo mostrar nos permite mostrar en la ventana la consulta
     * que se realiza en la base de datos en forma de tabla.
     * @param buscar. El parametro buscar se refiere al parametro por el cual se filtrara la consulta a la base de datos.
     */
    @Override
    public void mostrar(String buscar) {
        try {
            //se crea una tabla
            DefaultTableModel modelo;
            /*se crea un objeto de la clase CCompra para poder operar en la tabla compra.
            de la base de datos*/
            CCompra cc = new CCompra();
            // se almacena la tabla retornada al hacer una consulta en la tabla servicio.
            modelo = cc.consulta(buscar);
            //se muestra en la ventana la tabla recibida por la consulta.
            tablaCompras.setModel(modelo);
            //se obtiene el total de registros existentes en la tabla y se muestra en pantalla
            lbRegistros.setText("Total Registros: " + Integer.toString(cc.totalregistros));
            lbTR.setText("Total Reserv.: $ " + totalr.toString());
            lbTotal.setText("Total a pagar: $" +Double.toString(totalr + cc.totalConsumo));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }
