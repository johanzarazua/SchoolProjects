    private void tablaComprasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaComprasMouseClicked
        botonGuardar.setText("Editar");
        habilitar();
        botonEliminar.setEnabled(true);
        accion = "editar";
        int fila = tablaCompras.rowAtPoint(evt.getPoint());
        txtIDCompra.setText(tablaCompras.getValueAt(fila,0).toString());
        txtIDReservacion.setText(tablaCompras.getValueAt(fila,1).toString());
        txtIDServicio.setText(tablaCompras.getValueAt(fila,2).toString());
        txtServicio.setText(tablaCompras.getValueAt(fila,3).toString());
        txtCantidad.setText(tablaCompras.getValueAt(fila,5).toString());
        txtPrecio.setText(tablaCompras.getValueAt(fila,6).toString());
        cboEstado.setSelectedItem(tablaCompras.getValueAt(fila,7).toString());
    }//GEN-LAST:event_tablaComprasMouseClicked

    private void botonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEliminarActionPerformed
        if(!txtIDCompra.getText().equals("")){
            int decision = JOptionPane.showConfirmDialog(rootPane, "Deseas eliminra la compra?","Confirmar",2);
            if(decision == 0){
                CCompra cc = new CCompra();
                Compra c = new Compra();
                c.setIdCompra(Integer.parseInt(txtIDCompra.getText()));
                cc.eliminar(c);
                mostrar(idReservacion);
                inhabilitar();
            }
        }
    }//GEN-LAST:event_botonEliminarActionPerformed

