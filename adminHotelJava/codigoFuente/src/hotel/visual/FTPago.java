    public void mostrar(String buscar) {
         try {
            if (buscar.equals("")){
             //se crea una tabla
            DefaultTableModel modelo;
            /*se crea un objeto de la clase CServicio para poder operar en la tabla servicio.
            de la base de datos*/
            CPago cp = new CPago();
            // se almacena la tabla retornada al hacer una consulta en la tabla servicio.
            modelo = cp.consulta(buscar, "");
            //se muestra en la ventana la tabla recibida por la consulta.
            tbP.setModel(modelo);
            //se obtiene el total de registros existentes en la tabla y se muestra en pantalla
            lbRegistros.setText("Total pagos: " + Integer.toString(cp.totalregistros));
            }else{
            //se crea una tabla
            DefaultTableModel modelo;
            /*se crea un objeto de la clase CServicio para poder operar en la tabla servicio.
            de la base de datos*/
            CPago cp = new CPago();
            // se almacena la tabla retornada al hacer una consulta en la tabla servicio.
            modelo = cp.consulta2(buscar);
            //se muestra en la ventana la tabla recibida por la consulta.
            tbP.setModel(modelo);
            //se obtiene el total de registros existentes en la tabla y se muestra en pantalla
            lbRegistros.setText("Total pagos: " + Integer.toString(cp.totalregistros));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }
