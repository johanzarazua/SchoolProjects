    private void botonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEliminarActionPerformed
        if(!txtBuscar.getText().equals("")){
            int decision = JOptionPane.showConfirmDialog(rootPane, "Deseas eliminra el pago?","Confirmar",2);
            if(decision == 0){
                CPago cs = new CPago();
                Pago s = new Pago();
                s.setIdPago(Integer.parseInt(txtBuscar.getText()));
                cs.eliminar(s);
                mostrar("");
            }
        }
    }//GEN-LAST:event_botonEliminarActionPerformed

